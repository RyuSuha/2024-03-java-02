package com.green.day1;

//print access >> PrintAccess
public class Print {
    public static void main(String[] args){
        System.out.print("Hello!한글\n"); //"" 쌍따옴표는 문자열, String
        System.out.print("Hello!\t한글\n"); //"" 쌍따옴표는 문자열, String
        System.out.print("Hello!"); //파일이름과 클래스명은 동일해야한다
        System.out.print("Hello!"); // \n = 줄바꿈 (이스케이프 문자)
        System.out.print("Hello!\n");
        System.out.println("안녕\n");
        System.out.println("안녕");
        System.out.println("네");

    }
}
